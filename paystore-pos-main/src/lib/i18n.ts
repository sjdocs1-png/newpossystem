// Helper to get translation with English fallback
export const getTranslation = (key: string, language: LanguageCode): string => {
  return translations[language]?.[key] || translations.en[key] || key;
};

// Format currency based on country
export const formatCurrencyByCountry = (amount: number, countryCode: CountryCode): string => {
  const country = countries[countryCode];
  if (!country) return `${amount}`;
  
  try {
    return new Intl.NumberFormat(country.currency.locale, {
      style: 'currency',
      currency: country.currency.code,
      minimumFractionDigits: country.currency.code === 'OMR' ? 3 : 2,
      maximumFractionDigits: country.currency.code === 'OMR' ? 3 : 2,
    }).format(amount);
  } catch {
    return `${country.currency.symbol}${amount.toFixed(2)}`;
  }
};

// Get/Set stored preferences
export const getStoredCountry = (): CountryCode => {
  const stored = localStorage.getItem('pos_country');
  if (stored && stored in countries) return stored as CountryCode;
  return 'IN';
};

export const setStoredCountry = (code: CountryCode): void => {
  localStorage.setItem('pos_country', code);
};

